// Ejemplo de uso de let y const en JavaScript


//Cambiamos la variable nombre que estaba con var por let
let nombre = "Armando";
// Cambiamos la variable edad que estaba con var por const
const edad = 25;

nombre = "Ana María";

const saludo = "Hola, " + nombre + ". Tienes " + edad + " años.";

console.log(saludo); // muestra el contenido de saludo
